// Dynamically typed language
// Type of variable is determined during the run time

let name = "Mosh";
console.log(typeof name); // => string dtype
name = 12;
console.log(typeof name); /// => number dtype

let isValid = true;
console.log(typeof isValid); // => boolean dtype

let username;
console.log(typeof username); // => undefined dtype

let selectedColor = null;
console.log(typeof selectedColor); // => object dtype
