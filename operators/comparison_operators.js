/**
 * Comparison operator
 */
let x = 1;

// Relational
console.log(x > 0);
console.log(x >= 0);
console.log(x < 0);
console.log(x <= 0);

// Equality
console.log(x == 0);
console.log(x != 0);
console.log(x === 0); // equal and check dtype
console.log(x !== 0); // not equal date type check

console.log(1 === 1); // true
console.log("1" === 1); // false
console.log(1 == 1); // true
console.log("1" == 1); // true

console.log(true == 1); // true == true
// Strict equality === ensures both types have the same value
// Non strict equality operator == convert second operant into the same type first
